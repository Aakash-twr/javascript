// Strings - A collection of characters .

let a = "name";  // double quotes can be also used.
console.log(a.length)

let b = 'happy'; // single quotes can also be used.
console.log(b); // prints the string
console.log(b[0]) // elements can be accessed like arrays.

// Template Strings 

let name1 = "boy1";
let name2 = "boy2";
console.log(`${name1} is a friend of ${name2}`);  // backticks is used in template strings.

// Escape sequence characters -

console.log('adam\'s'); // \ - used to add ' single quotes to the string.
console.log("evening\n"); // \n - means new line is added after the string.
