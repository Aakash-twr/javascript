// Methods used in strings

// 1. length - calculate the length of string.

let a = "every";
console.log(a.length); // 5

// 2. toUpperCase - converts into Uppercase

console.log(a.toUpperCase());

// same goes for lowercase.

// 3. slice - It cut the string from index1 to index2 in which index2 is not included.

console.log(a.slice(2,4)); // from index 2 to index3.
console.log(a.slice(2)); // from index2 to last

// 3. replace - replace the exact string in the original string.

console.log(a.replace("ev","hp"));

// 4 . trim - trim the extra spaces;

let b = "    game on      ";
console.log(b);
console.log(b.trim());

